package org.opentutorials.javatutorials.variables;

public class DivisionDemo {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		float c = 10.0F;
		float d = 3.0F;
		
		System.out.println(a/b); //result = 3
		System.out.println(c/d); //result = 3.33
		System.out.println(a/d); //result = 3.33

	}

}
